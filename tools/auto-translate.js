#!/usr/bin/env node
// Fills missing FR translations by calling MyMemory (free, no API key).
// Reads: assets/translations.js + assets/translations-overrides.js
// Writes: assets/translations-auto.js
// Manual FR entries always win — this script only fills gaps.
// Usage: node tools/auto-translate.js [--dry] [--force]

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const ROOT = path.resolve(__dirname, '..');
const TRANS_FILE = path.join(ROOT, 'assets/translations.js');
const OVERRIDES_FILE = path.join(ROOT, 'assets/translations-overrides.js');
const AUTO_FILE = path.join(ROOT, 'assets/translations-auto.js');

const DRY = process.argv.includes('--dry');
const FORCE = process.argv.includes('--force');

function loadTranslations() {
  // Strip leading `const`/`let`/`var` so the assignment lands on the sandbox global.
  const transSrc = fs.readFileSync(TRANS_FILE, 'utf8').replace(/^\s*(?:const|let|var)\s+EU_TRANSLATIONS/, 'EU_TRANSLATIONS');
  const sandbox = { EU_TRANSLATIONS: null, console };
  vm.createContext(sandbox);
  vm.runInContext(transSrc, sandbox);

  if (fs.existsSync(OVERRIDES_FILE)) {
    const ovSrc = fs.readFileSync(OVERRIDES_FILE, 'utf8');
    try {
      vm.runInContext(ovSrc, sandbox);
    } catch (e) {
      console.warn(`[warn] translations-overrides.js failed to parse: ${e.message}`);
      console.warn('       Its entries will NOT be considered. Fix that file and rerun.');
    }
  }

  if (fs.existsSync(AUTO_FILE)) {
    const autoSrc = fs.readFileSync(AUTO_FILE, 'utf8');
    try {
      vm.runInContext(autoSrc, sandbox);
    } catch (e) {
      console.warn(`[warn] existing translations-auto.js failed to parse: ${e.message}`);
    }
  }

  return sandbox.EU_TRANSLATIONS;
}

function flatten(obj, prefix = '') {
  const out = {};
  for (const k of Object.keys(obj)) {
    const v = obj[k];
    const key = prefix ? `${prefix}.${k}` : k;
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      Object.assign(out, flatten(v, key));
    } else {
      out[key] = v;
    }
  }
  return out;
}

async function translate(text) {
  const url =
    'https://api.mymemory.translated.net/get?q=' +
    encodeURIComponent(text) +
    '&langpair=en|fr';
  const res = await fetch(url, { headers: { 'User-Agent': 'editorsunited-auto-translate' } });
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  const json = await res.json();
  if (json.responseStatus !== 200 && json.responseStatus !== '200') {
    throw new Error(json.responseDetails || 'unknown error');
  }
  return json.responseData.translatedText;
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function emitAutoFile(results) {
  const header = [
    '// AUTO-GENERATED by tools/auto-translate.js — do not edit by hand.',
    '// Manual FR entries in translations.js / translations-overrides.js always win.',
    '// Regenerate: `node tools/auto-translate.js`',
    '(function () {',
    '  if (typeof EU_TRANSLATIONS === "undefined") return;',
    '  if (!EU_TRANSLATIONS.fr) EU_TRANSLATIONS.fr = {};',
    '  function setIfMissing(path, value) {',
    '    const keys = path.split(".");',
    '    let obj = EU_TRANSLATIONS.fr;',
    '    for (let i = 0; i < keys.length - 1; i++) {',
    '      if (!obj[keys[i]] || typeof obj[keys[i]] !== "object") obj[keys[i]] = {};',
    '      obj = obj[keys[i]];',
    '    }',
    '    const last = keys[keys.length - 1];',
    '    if (obj[last] === undefined || obj[last] === "") obj[last] = value;',
    '  }',
  ];
  const body = Object.entries(results).map(
    ([k, v]) => `  setIfMissing(${JSON.stringify(k)}, ${JSON.stringify(v)});`
  );
  const footer = ['})();', ''];
  return header.concat(body, footer).join('\n');
}

function loadExistingAutoResults() {
  if (!fs.existsSync(AUTO_FILE)) return {};
  const src = fs.readFileSync(AUTO_FILE, 'utf8');
  const out = {};
  const re = /setIfMissing\(\s*("(?:[^"\\]|\\.)*")\s*,\s*("(?:[^"\\]|\\.)*")\s*\)/g;
  let m;
  while ((m = re.exec(src)) !== null) {
    try {
      out[JSON.parse(m[1])] = JSON.parse(m[2]);
    } catch {}
  }
  return out;
}

async function main() {
  const translations = loadTranslations();
  if (!translations || !translations.en || !translations.fr) {
    console.error('Could not load EU_TRANSLATIONS.en / .fr');
    process.exit(1);
  }

  const enFlat = flatten(translations.en);
  const frFlat = flatten(translations.fr);
  const existingAuto = FORCE ? {} : loadExistingAutoResults();

  const missing = [];
  for (const [key, enVal] of Object.entries(enFlat)) {
    if (typeof enVal !== 'string') continue;
    const frVal = frFlat[key];
    // Treat any non-empty FR (even one identical to EN) as intentional — proper nouns,
    // brand terms, numbers, and ★ ratings often legitimately match across languages.
    const hasFr = frVal !== undefined && frVal !== '' && !(key in existingAuto);
    if (hasFr) continue;
    if (!FORCE && existingAuto[key]) continue;
    missing.push({ key, en: enVal });
  }

  console.log(`Scanned ${Object.keys(enFlat).length} EN keys. Missing FR: ${missing.length}`);
  if (missing.length === 0) {
    if (!DRY) {
      fs.writeFileSync(AUTO_FILE, emitAutoFile(existingAuto));
      console.log(`Wrote ${Object.keys(existingAuto).length} carry-over entries to ${AUTO_FILE}`);
    }
    console.log('Nothing new to translate. Done.');
    return;
  }
  if (DRY) {
    for (const { key, en } of missing) console.log(`  would translate: ${key} — ${en.slice(0, 60)}`);
    return;
  }

  const results = { ...existingAuto };
  let ok = 0;
  let fail = 0;

  for (let i = 0; i < missing.length; i++) {
    const { key, en } = missing[i];
    try {
      const fr = await translate(en);
      results[key] = fr;
      ok++;
      console.log(`  [${i + 1}/${missing.length}] ${key}: ${en.slice(0, 50)} -> ${fr.slice(0, 50)}`);
    } catch (e) {
      fail++;
      console.log(`  [${i + 1}/${missing.length}] ${key}: FAIL ${e.message}`);
    }
    await sleep(250);
  }

  fs.writeFileSync(AUTO_FILE, emitAutoFile(results));
  console.log(`\nWrote ${Object.keys(results).length} entries to ${AUTO_FILE}`);
  console.log(`Success: ${ok}  Failed: ${fail}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
